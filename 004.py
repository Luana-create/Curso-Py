a= input('Digite algo ')#a é um objeto
print('O tipo primitivo desse valor é? ', type(a))
print('Só tem espaços? ', a.isspace())#são métodos de a todos esses is
print("É alfanumerico? ", a.isalnum())
print('É um alfabetico? ', a.isalpha())
print ('Esta em maiúscula? ', a.isupper())
print ('Esta em minúscula? ', a.islower())
print('Esta capitalizado? ', a.istitle())#Nem maiuscula e nem minúscula